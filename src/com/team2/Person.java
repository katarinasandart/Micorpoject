package com.team2;

public class Person {
    public int x;
    public int y;
    public Person(){
        this.x = 40;
        this.y = 10;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}
